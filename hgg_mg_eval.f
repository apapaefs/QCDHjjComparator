      PROGRAM HGG_MG_EVAL
      IMPLICIT NONE
      INTEGER NEXTERNAL,NINCOMING
      PARAMETER (NEXTERNAL=5,NINCOMING=2)
      INCLUDE 'coupl.inc'
      INCLUDE 'ngraphs.inc'
      INCLUDE 'nsquaredSO.inc'
      INTEGER I,J,IOS,EVENT,RETURNCODE,MADIM,NSQSO_LOOP
      CHARACTER*32 CATEGORY
      DOUBLE PRECISION P(0:3,NEXTERNAL),ALPHAS_IN,MUR
      DOUBLE PRECISION,ALLOCATABLE :: MATELEM(:,:)
      DOUBLE PRECISION,ALLOCATABLE :: PREC_FOUND(:)
      LOGICAL INIT
      DATA INIT/.TRUE./
      COMMON/INITCHECKSA/INIT
      LOGICAL CHOSEN_LOOP_SO_CONFIGS(NSQUAREDSO)
      COMMON/ML5_1_0_1_CHOSEN_LOOP_SQSO/CHOSEN_LOOP_SO_CONFIGS

      IF (INIT) THEN
         INIT=.FALSE.
         CALL ML5_1_0_1_GET_ANSWER_DIMENSION(MADIM)
         ALLOCATE(MATELEM(0:3,0:MADIM))
         CALL ML5_1_0_1_GET_NSQSO_LOOP(NSQSO_LOOP)
         ALLOCATE(PREC_FOUND(0:NSQSO_LOOP))
         CALL SETPARA('../../Cards/param_card.dat')
      ENDIF

      OPEN(10,FILE='hgg_mg_points.dat',STATUS='OLD',ACTION='READ')
      OPEN(20,FILE='hgg_mg_eval.out',STATUS='UNKNOWN',ACTION='WRITE')
 10   CONTINUE
      READ(10,*,IOSTAT=IOS) EVENT,CATEGORY,ALPHAS_IN,MUR,
     $   ((P(J,I),J=0,3),I=1,NEXTERNAL)
      IF (IOS.NE.0) GOTO 99
      CALL UPDATE_AS_PARAM2(MUR*MUR,ALPHAS_IN)
      CALL ML5_1_0_1_SLOOPMATRIX_THRES(P,MATELEM,-1.0D0,
     $   PREC_FOUND,RETURNCODE)
      WRITE(20,'(I8,1X,A32,1X,ES24.16,1X,ES24.16,1X,I8,1X,ES12.4)')
     $   EVENT,CATEGORY,MATELEM(1,0),MATELEM(1,1),RETURNCODE,
     $   PREC_FOUND(0)
      GOTO 10
 99   CONTINUE
      CLOSE(10)
      CLOSE(20)
      END
